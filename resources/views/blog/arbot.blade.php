                <h2 class="topic2-text">One-way Strategy</h2>
                <p class="article-text">This is one of the most arbitrage strategy, refers to the purchase of one particular cryptocurrency (Bitcoin,
                    Ethereum, Litecoin, etc) from a market with a lower market value and reselling it in another market with
                    a higher value. This provides opportunities to take advantage of differing prices to generate a profit
                    by buying low and selling high. This concept is the easiest and most straightforward arbitrage trading.
                    However, this would also mean that the fiat currency (often USD) would be hold in destination exchange
                    until the price in two exchanges is stabilized in order to transfer asset, or to withdraw it and pay
                    the huge bank transfer fees and exchange rate (in some case) to complete the loop. Furthermore, one-way
                    can be performed by two or three times, to get the funds back to the starting exchange.
